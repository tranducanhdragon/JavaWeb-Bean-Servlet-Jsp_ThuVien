package Model;

/**
 *
 * @author DucAnhTran
 */
public class Muon {
    private String idMuon;
    private String idThe;
    private String maSach;
    private int soluong;

    public Muon() {
    }

    public String getIdMuon() {
        return idMuon;
    }

    public void setIdMuon(String idMuon) {
        this.idMuon = idMuon;
    }

    public String getIdThe() {
        return idThe;
    }

    public void setIdThe(String idThe) {
        this.idThe = idThe;
    }

    public String getMaSach() {
        return maSach;
    }

    public void setMaSach(String maSach) {
        this.maSach = maSach;
    }

    public int getSoluong() {
        return soluong;
    }

    public void setSoluong(int soluong) {
        this.soluong = soluong;
    }
    
    
}
